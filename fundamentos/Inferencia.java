package fundamentos;

public class Inferencia {

	public static void main(String[] args) {
		
		double a = 5.6;
		System.out.println(a);
		
		a = 12;
		System.out.println(a);
		
		var b = 4.5;
		System.out.println(b);
		
		var c = "texto1";
		System.out.println(c);
		
		c = "Outro texto";
		System.out.println(c);
		
		double d;
		d = 4.5;
		System.out.println(d);
		
		
				
	}
}
